<div class="text-box" id="formats">
    <p class="titre-paragraphe">Les formats de MTG</p>
    <p>Il existe différentes façons de jouer à Magic: The Gathering. Ces différentes façons s'appellent des formats.</p>
    <p>Voici donc les formats les plus populaires dans MTG : </p>
</div>
<div class="text-box" id="standard">
    <p class="titre-paragraphe">STANDARD</p>
    <p>Le format STANDARD est le format classique de MTG. Il se joue à 2 joueurs</p>
    <p>Chaque joueur possède un deck de 60 cartes minimum, et commence avec 20 points de vies.</p>
    <p>Le format STANDARD possède ce qu'on appelle une "rotation".</p>
    <p>Tous les ans, les cartes tournent. Les plus anciennes ne sont plus légales.</p>
    <p>C'est environ 4 à 5 extensions qui sortent du format tous les ans.</p>
    <p>En général, les nouvelles extensions qui viennent en STANDARD restent environ 3 ans.</p>
</div>
<div class="text-box" id="commander">
    <p class="titre-paragraphe">COMMANDER</p>
    <p>Le format COMMANDER (ou EDH) est sans aucun doute le format le plus joué de MTG. Il se joue à 4 joueurs</p>
    <p>Chaque joueur un deck d'exactement 100 cartes, dont une créature légendaire qui commence dehors, appelé "commandant", et chaque joueur a 40 points de vie.</p>
    <p>Le format COMMANDER est ce qu'on appelle un "format éternel".</p>
    <p>Toutes les carte MTG qui on déjà existés (sauf les "unsets") sont et seront toujours légales en COMMANDER.</p>
</div>
<div class="text-box" id="pauper">
    <p class="titre-paragraphe">PAUPER</p>
    <p>Le format PAUPER est un format éternel.</p>
    <p>Il est similaire au format STANDARD, excepté que vous ne pouvez jouer que les cartes de rarté "courante".</p>
</div>
<div class="text-box" id="sealed">
    <p class="titre-paragraphe">SEALED</p>
    <p>Le format SEALED fait partie des formats dits "limité".</p>
    <p>Chaque joueur reçoit un paquet contenant des boosters de cartes, et doit constituer un deck de 40 cartes minimum uniquement avec ces cartes.</p>
    <p>Il peut y avoir plusieurs joueurs dans ce format, sans limite.</p>
</div>