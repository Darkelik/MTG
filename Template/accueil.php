<div class="text-box" id="phases">
    <p class="titre-paragraphe">Les phases et étapes du jeu</p>
    <p>Dans Magic, chaque tour est divisé en plusieurs phases, qui sont eux-mêmes divisés en plusieurs étapes :</p>
    <p class="titre-paragraphe">La phase de début</p>
    <p>La phase de début est la phase où un joueur se prépare au début de son tour. Elle comporte 3 étapes : </p>
    <ul class="liste">
        <li>L'étape de dégagement. Toutes vos cartes se dégagent.</li>
        <li>L'étape d'entretien. Certaines capacitées se déclenchent.</li>
        <li>L'étape de pioche. Vous piochez une carte.</li>
    </ul>
    <p class="titre-paragraphe">La première phase principale</p>
    <p>La première phase principale est le moment où vous jouez vos terrains et lancez vos sorts.</p>
    <p>C'est là qu'il faut commencer à construire le champ de bataille.</p>
    <p>Pour lancer un sort, payez son coût en engageant vos terrains.</p>
    <p class="titre-paragraphe">La phase de combat</p>
    <p>La phase de combat est le moment où vous allez attaquer votre adversaire pour lui infliger des blessures. Elle comporte 5 étapes :</p>
    <ul class="liste">
        <li>L'étape de début du combat. Certaines capacités se déclenchent.</li>
        <li>L'étape de déclaration des attaquants. Vous attaquez avec vos créatures.</li>
        <li>L'étape de déclaration des bloqueurs. Vos adversaires bloquent avec leurs créatures.</li>
        <li>L'étape de blessures. Les blessures de combat sont infligées.</li>
        <li>L'étape de fin du combat. Certaines capacités se déclenchent.</li>
    </ul>
    <p>Pour attaquer votre adversaire, vous devez engager vos créatures dégagées. Vos adversaires peuvent bloquer vos créatures attaquantes avec leurs créatures dégagées.</p>
    <p>Au moment où on passe à l'étape de blessures, chaque créature attaquante non bloquée inflige ses blessures à votre adversaire, et chaque créature bloqueuse ou bloquée inflige ses blessures à l'autre.</p>
    <p>Chaque créature ne peut bloquer qu'une seule créature, et plusieurs créatures peuvent bloquer la même créature. Si c'est le cas, la créature attaquante répartit ses blessures sur ces créatures.</p>
    <p>Chaque créature dispose d'une force et d'une endurance. Sa force représente le nombre de blessures de combat qu'elle va infliger, tandis que son endurance représente le nombre de blessure qu'elle doit subir en un tour pour mourir.</p>
    <p class="titre-paragraphe">La deuxième phase principale</p>
    <p>Tout comme la première phase principale, la deuxième phase principale vous permet de jouer des terrains et lancer des sorts. N'oubliez pas que vous ne pouvez jouer qu'un seul terrain par tour.</p>
    <p class="titre-paragraphe">La phase de fin</p>
    <p>La phase de fin marque la fin de votre tour, avant de passer à celle de votre adversaire. Elle comporte 2 étapes :</p>
    <ul class="liste">
        <li>L'étape de fin. Certaines capacités se déclenchent.</li>
        <li>L'étape de nettoyage. Les blessures de vos créatures guérissent.</li>
    </ul>
    <p>De plus, pendant l'étape de nettoyage, si vous avez plus de 7 cartes en main, vous devez vous en défausser jusqu'à en avoir 7.</p>
