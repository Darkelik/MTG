<div class="text-box" id="histoire">
    <p class="titre-paragraphe">Parlons du meilleur jeu de TCG qui existe : MTG</p>
    <p>Magic: The Gathering (ou Magic: l'Assemblée en français) est un jeu de cartes à collectionner créé en 1993 par Richard Garield.</p>
    <p>Ce jeu est donc plus vieux que Yu-Gi-Oh de 3 ans (qui est aussi un TCG).</p>
    <p>Avec plus de 50000 cartes différentes, MTG permet aux joueurs de créer leur propres decks de cartes avec un thème unique à chacun.</p>
    <p>Wizards of the Coast (WOTC) est l'éditeur officiel de MTG, et publie chaque trimestre une nouvelle extension contenant de nouvelles cartes, permettant d'enrichir l'aspect du jeu et d'étendre sa communauté.</p>
    <p>Les cartes sont divisées en 4 raretés différentes, indiquant ainsi leur puissance globale :</p>
    <ul class="liste">
        <li>Commun (C), représenté par un symbole noir;</li>
        <li>Inhabituel (U), représenté par un symbole gris;</li>
        <li>Rare (R), représenté par un symbole jaune;</li>
        <li>Rare Mythique (M), représenté par un symbole orange.</li>
    </ul>
</div>
<div class="text-box" id="regles">
    <p class="titre-paragraphe">Les règles de base</p>
    <p>Le but du jeu : réduire les points de vie de chaque adversaire à 0.</p>
    <p>Chaque joueur commence avec un nombre de points de vie qui dépend du format joué (généralement 20).</p>
    <p>Chaque joueur construit un deck contenant un nombre de cartes qui dépend du format (en général, minimum 60).</p>
    <p>Durant chaque tour, le joueur actif pioche une carte et lance des sorts pour combattre son adversaire.</p>
    <p>Il existe 5 couleurs dans MTG qui ont chacun leur propre pouvoir : <font color="yellow">Blanc</font>, <font color="blue">Bleu</font>, <font color="purple">Noir</font>, <font color="red">Rouge</font> et <font color="green">Vert</font></p>
